export function generateTestJpegFrame(cameraId: string, width: number = 640, height: number = 480): Buffer {
  // For now, return a simple test JPEG
  return createMinimalJpeg(cameraId);
}

function createMinimalJpeg(cameraId: string): Buffer {
  // Create a simple test pattern JPEG
  // This is a basic 64x64 pixel JPEG with a gradient pattern
  const time = Date.now() / 1000;
  const idSeed = cameraId.split('').reduce((acc, c) => acc + c.charCodeAt(0), 0);
  const colorValue = Math.floor((Math.sin(time + idSeed) + 1) * 127); // Animated color unique per camera
  
  // Create a minimal but valid JPEG with animated content
  const jpegData = Buffer.from([
    0xFF, 0xD8, // SOI (Start of Image)
    0xFF, 0xE0, // APP0
    0x00, 0x10, // Length (16 bytes)
    0x4A, 0x46, 0x49, 0x46, 0x00, // "JFIF\0"
    0x01, 0x01, // Version 1.1
    0x01, // Units (1 = dots per inch)
    0x00, 0x48, 0x00, 0x48, // X and Y density (72 DPI)
    0x00, 0x00, // Thumbnail width and height (0 = no thumbnail)
    
    // Quantization table
    0xFF, 0xDB, // DQT
    0x00, 0x43, // Length (67 bytes)
    0x00, // Precision and table ID
    // Standard luminance quantization table (simplified)
    0x10, 0x0B, 0x0C, 0x0E, 0x0C, 0x0A, 0x10, 0x0E,
    0x0D, 0x0E, 0x12, 0x11, 0x10, 0x13, 0x18, 0x28,
    0x1A, 0x18, 0x16, 0x16, 0x18, 0x31, 0x23, 0x25,
    0x1D, 0x28, 0x3A, 0x33, 0x3D, 0x3C, 0x39, 0x33,
    0x38, 0x37, 0x40, 0x48, 0x5C, 0x4E, 0x40, 0x44,
    0x57, 0x45, 0x37, 0x38, 0x50, 0x6D, 0x51, 0x57,
    0x5F, 0x62, 0x67, 0x68, 0x67, 0x3E, 0x4D, 0x71,
    0x79, 0x70, 0x64, 0x78, 0x5C, 0x65, 0x67, 0x63,
    
    // Start of Frame
    0xFF, 0xC0, // SOF0
    0x00, 0x11, // Length (17 bytes)
    0x08, // Precision (8 bits)
    0x00, 0x40, 0x00, 0x40, // Height and width (64x64)
    0x01, // Number of components
    0x01, 0x11, 0x00, // Component 1: Y, sampling factors, quantization table
    
    // Huffman table (simplified)
    0xFF, 0xC4, // DHT
    0x00, 0x1F, // Length
    0x00, // Table class and destination
    0x00, 0x01, 0x05, 0x01, 0x01, 0x01, 0x01, 0x01,
    0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
    0x08, 0x09, 0x0A, 0x0B,
    
    // Start of Scan
    0xFF, 0xDA, // SOS
    0x00, 0x0C, // Length
    0x01, // Number of components
    0x01, 0x00, // Component 1, Huffman tables
    0x00, 0x3F, 0x00, // Start, end, approximation
    
    // Compressed data (minimal)
    colorValue, 0x00, 0x00, 0x00,
    
    0xFF, 0xD9  // EOI (End of Image)
  ]);
  
  return jpegData;
}