// Simple test image generator for camera streams
export function generateTestJpegFrame(cameraId) {
  // Create a simple colored JPEG pattern based on camera ID
  const time = Date.now();
  const cameraNum = cameraId.replace('cam', '');
  
  // Generate different colors for different cameras
  const colors = {
    '1': [255, 100, 100], // Red for cam1
    '2': [100, 255, 100], // Green for cam2
  };
  
  const [r, g, b] = colors[cameraNum] || [150, 150, 255];
  
  // Create a simple animated pattern
  const phase = (time / 100) % (Math.PI * 2);
  const brightness = Math.sin(phase) * 50 + 50;
  
  // Create a simple valid JPEG using a minimal pattern
  // This creates a tiny 1x1 pixel JPEG with the specified color
  const jpegData = Buffer.from([
    0xFF, 0xD8, // SOI marker
    
    0xFF, 0xE0, 0x00, 0x10, // APP0 marker
    0x4A, 0x46, 0x49, 0x46, 0x00, // 'JFIF' identifier
    0x01, 0x01, // Version
    0x01, // Density units (1 = no units)
    0x00, 0x48, 0x00, 0x48, // X density (72 DPI)
    0x00, 0x48, 0x00, 0x48, // Y density (72 DPI)
    0x00, 0x00, // Thumbnail width (0 = no thumbnail)
    0x00, 0x00, // Thumbnail height (0 = no thumbnail)
    
    0xFF, 0xDB, 0x00, 0x43, // Quantization table marker
    // Standard quantization table
    0x00, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
    0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
    0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
    0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
    0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
    
    0xFF, 0xC0, 0x00, 0x11, // Start of frame marker
    0x08, // Precision (8 bits)
    0x00, 0x01, // Height (1)
    0x00, 0x01, // Width (1)
    0x03, // Number of components (3 = RGB)
    0x01, 0x11, 0x00, // Component 1 (Y)
    0x02, 0x11, 0x01, // Component 2 (Cb)
    0x03, 0x11, 0x01, // Component 3 (Cr)
    
    0xFF, 0xC4, 0x00, 0x14, // Huffman table marker (DC luminance)
    0x00, 0x01, // Table class (0 = DC), destination ID (0)
    0x00, 0x00, 0x01, 0x05, 0x01, 0x01, 0x01, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x01, 0x02, 0x03, 0x04, 0x05,
    
    0xFF, 0xC4, 0x00, 0x14, // Huffman table marker (AC luminance)
    0x10, 0x01, // Table class (1 = AC), destination ID (0)
    0x00, 0x02, 0x01, 0x03, 0x03, 0x02, 0x04, 0x03, 0x05, 0x05, 0x04, 0x04, 0x00, 0x00, 0x01, 0x7D, 0x01, 0x02, 0x03,
    
    0xFF, 0xC4, 0x00, 0x14, // Huffman table marker (DC chrominance)
    0x01, 0x01, // Table class (0 = DC), destination ID (1)
    0x00, 0x00, 0x01, 0x05, 0x01, 0x01, 0x01, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x01, 0x02, 0x03, 0x04, 0x05,
    
    0xFF, 0xC4, 0x00, 0x14, // Huffman table marker (AC chrominance)
    0x11, 0x01, // Table class (1 = AC), destination ID (1)
    0x00, 0x02, 0x01, 0x03, 0x03, 0x02, 0x04, 0x03, 0x05, 0x05, 0x04, 0x04, 0x00, 0x00, 0x01, 0x7D, 0x01, 0x02, 0x03,
    
    0xFF, 0xDA, 0x00, 0x0C, // Start of scan marker
    0x03, // Number of components
    0x01, 0x00, 0x02, 0x11, 0x03, 0x11, // Component selection
    0x00, 0x3F, 0x00, // Start of spectral selection, successive approximation
    
    // Simple image data - create a pattern with the camera color
    Math.floor((r + brightness) / 4) % 256, // DC coefficient for Y
    ...Array(63).fill(0x00), // AC coefficients
    
    Math.floor((g + brightness) / 4) % 256, // DC coefficient for Cb
    ...Array(63).fill(0x00), // AC coefficients
    
    Math.floor((b + brightness) / 4) % 256, // DC coefficient for Cr
    ...Array(63).fill(0x00), // AC coefficients
    
    0xFF, 0xD9 // EOI marker (End of Image)
  ]);
  
  return jpegData;
}